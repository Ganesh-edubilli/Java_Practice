//write a Java Program to print all alphabets from a to z

//importing the Scanner class
import java.util.Scanner;
// positive class
class Alphabets
  {
    //main method
    public static void main(String[] args)
    {
      char ch;
      for(ch='a';ch<='z';ch++)
      {
        System.out.println(ch);
      }
    }
  }